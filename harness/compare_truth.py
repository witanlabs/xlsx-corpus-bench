#!/usr/bin/env python3
"""Judge every calculation engine against the Excel-recomputed truth corpus.

For each library, produce a recalculated copy of every corpus workbook using
that library's engine, then compare the formula-cell values it wrote against
the values Excel wrote for the same file (results/excel-truth/<sha>.xlsx,
generated by harness/excel_truth.py). Extraction and comparison both happen
in harness code (cached_values.py) — no engine judges its own work, and all
engines face the identical comparator:

  - denominator: truth formula cells where Excel produced a value, minus
    volatile formulas (NOW/TODAY/RAND*)
  - a cell the engine left valueless or dropped counts as a mismatch
  - numbers compared with 1e-9 relative tolerance, strings exact

Engines:
  witan       — `xlsx-serve calc <copy>` (rewrites cached values in place)
  libreoffice — headless conversion with recalc-on-load forced
  epplus      — DotnetRunner --mode recalc-save (Workbook.Calculate)
  closedxml   — DotnetRunner --mode recalc-save (RecalculateAllFormulas)

Output: results/truth-compare/<lib>.jsonl, one record per workbook that has
a truth file. Resumable per library.

Usage: compare_truth.py --lib {witan,libreoffice,epplus,closedxml}
"""
import argparse
import json
import os
import shutil
import subprocess
import sys
import tempfile

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, os.path.join(os.path.dirname(os.path.abspath(__file__)), "runners"))
from cached_values import compare_to_truth, extract  # noqa: E402

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
XLSX_SERVE = os.environ.get(
    "WITAN_XLSX_SERVE", os.path.join(ROOT, "..", "witan-alfred", "bin", "publish", "xlsx-serve")
)
DOTNET_RUNNER = os.path.join(ROOT, "harness/runners/dotnet/bin/Release/net10.0/DotnetRunner")
PER_FILE_TIMEOUT_S = 120


def witan_recalc(records, recalc_dir):
    for rec in records:
        out = os.path.join(recalc_dir, rec["sha256"] + ".xlsx")
        if os.path.exists(out):
            continue
        shutil.copy(os.path.join(CORPUS, rec["path"]), out)
        try:
            subprocess.run(
                [XLSX_SERVE, "--json", "calc", out],
                capture_output=True,
                timeout=PER_FILE_TIMEOUT_S,
            )
        except subprocess.TimeoutExpired:
            os.unlink(out)  # absent file = engine produced no recalc output


def libreoffice_recalc(records, recalc_dir):
    from runner_libreoffice import RECALC_ALWAYS, convert_with_retry

    todo = [r for r in records if not os.path.exists(os.path.join(recalc_dir, r["sha256"] + ".xlsx"))]
    with tempfile.TemporaryDirectory(prefix="lo_truth_") as work:
        stage = os.path.join(work, "stage")
        os.makedirs(stage)
        for i in range(0, len(todo), 25):
            batch = todo[i : i + 25]
            staged = []
            for rec in batch:
                dst = os.path.join(stage, rec["sha256"] + rec["ext"])
                shutil.copy(os.path.join(CORPUS, rec["path"]), dst)
                staged.append(dst)
            convert_with_retry(staged, recalc_dir, work, f"t{i}", RECALC_ALWAYS)
            for f in staged:
                os.unlink(f)
            print(f"libreoffice recalc: {min(i + 25, len(todo))}/{len(todo)}", file=sys.stderr)


def dotnet_recalc_emit(lib, records, emit_path):
    """Emit computed values as JSON rather than judging through SaveAs —
    ClosedXML doesn't persist cached formula values and EPPlus only
    partially, which would conflate computation with serialization."""
    done = set()
    if os.path.exists(emit_path):
        with open(emit_path) as f:
            for line in f:
                try:
                    done.add(json.loads(line)["sha256"])
                except Exception:
                    pass
    with tempfile.NamedTemporaryFile("w", suffix=".jsonl", delete=False) as mf:
        for rec in records:
            if rec["sha256"] not in done:
                mf.write(json.dumps(rec) + "\n")
        manifest = mf.name
    cmd = [
        DOTNET_RUNNER, "--lib", lib, "--mode", "recalc-emit",
        "--corpus", CORPUS, "--manifest", manifest,
        "--out", emit_path, "--out-dir", os.path.dirname(emit_path),
    ]
    for _attempt in range(1000):
        proc = subprocess.run(cmd)
        if proc.returncode != 3:
            break
        print("dotnet recalc-emit restarting after wedged file", file=sys.stderr)
    os.unlink(manifest)


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--lib", required=True, choices=["witan", "libreoffice", "epplus", "closedxml"])
    ap.add_argument("--results-dir", default="results-sb")
    ap.add_argument("--corpus", default="corpus-sb")
    args = ap.parse_args()

    global RESULTS, TRUTH_DIR, CORPUS
    RESULTS = os.path.join(ROOT, args.results_dir)
    TRUTH_DIR = os.path.join(RESULTS, "excel-truth")
    CORPUS = os.path.join(ROOT, args.corpus)
    args.manifest = os.path.join(RESULTS, "manifest.jsonl")

    records = [json.loads(l) for l in open(args.manifest)]
    with_truth = [r for r in records if os.path.exists(os.path.join(TRUTH_DIR, r["sha256"] + ".xlsx"))]
    print(f"{len(with_truth)}/{len(records)} corpus files have Excel truth", file=sys.stderr)

    recalc_dir = os.path.join(RESULTS, "recalc-out", args.lib)
    os.makedirs(recalc_dir, exist_ok=True)
    emitted = None  # sha -> {addr: (kind, value)} for the dotnet libs
    if args.lib == "witan":
        witan_recalc(with_truth, recalc_dir)
    elif args.lib == "libreoffice":
        libreoffice_recalc(with_truth, recalc_dir)
    else:
        emit_path = os.path.join(recalc_dir, "emitted.jsonl")
        dotnet_recalc_emit(args.lib, with_truth, emit_path)
        emitted = {}
        with open(emit_path) as f:
            for line in f:
                rec = json.loads(line)
                if rec.get("cells") is not None:
                    emitted[rec["sha256"]] = {
                        a: tuple(v) for a, v in rec["cells"].items()
                    }

    out_dir = os.path.join(RESULTS, "truth-compare")
    os.makedirs(out_dir, exist_ok=True)
    out_path = os.path.join(out_dir, f"{args.lib}.jsonl")
    done = set()
    if os.path.exists(out_path):
        with open(out_path) as f:
            for line in f:
                try:
                    done.add(json.loads(line)["sha256"])
                except Exception:
                    pass

    n = 0
    with open(out_path, "a") as out:
        for rec in with_truth:
            sha = rec["sha256"]
            if sha in done:
                continue
            res = {"sha256": sha, "path": rec["path"], "lib": args.lib, "ok": False, "error": None}
            recalced = os.path.join(recalc_dir, sha + ".xlsx")
            try:
                truth = extract(os.path.join(TRUTH_DIR, sha + ".xlsx"), with_formula=True)
                engine = None
                if emitted is not None:
                    engine = emitted.get(sha)
                elif os.path.exists(recalced):
                    try:
                        engine = extract(recalced)
                    except Exception:
                        engine = None
                if engine is None:
                    # engine produced nothing usable: penalized, not excluded
                    # — every tool is judged on the exact same file set, so a
                    # failed file counts as all-mismatch rather than vanishing
                    # from this engine's denominator
                    res.update(compare_to_truth(truth, {}))
                    res["engine_failed"] = True
                else:
                    res.update(compare_to_truth(truth, engine))
                res["ok"] = True
            except Exception as e:
                res["error"] = f"{type(e).__name__}: {e}"[:500]
            out.write(json.dumps(res) + "\n")
            n += 1
            if os.path.exists(recalced):
                os.unlink(recalced)
    print(f"{args.lib}: {n} new truth comparisons -> {out_path}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
